public class Pattern_1 {
    public static void main(String args[]){
        int i,j,k=6;
        System.out.println(k);
        for(i=1;i<=5;i++){
            for(j=1;j<=i;j++){
                System.out.print(j);
            }
            System.out.println(k);
        }
        System.out.println();
    }
}
